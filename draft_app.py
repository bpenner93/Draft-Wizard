def build_rookie_board(board):
    """Rookie-draft view: rookies only, valued by the REAL dynasty market
    (DynastyProcess superflex value) when available, else our dynasty value.
    Pseudo rookie-ADP = that value's rank (no public rookie-draft ADP feed)."""
    rb = []
    for p in board:
        if not p.get("rookie"):
            continue
        q = dict(p)
        q["pts"] = p.get("dp2") or p.get("dp1") or p.get("dyn") or p.get("pts") or 0.0
        q["rec"] = 0.0
        rb.append(q)
    rb.sort(key=lambda x: -x["pts"])
    for i, q in enumerate(rb, 1):
        q["adp"], q["adp_sd"], q["adp_sf"] = float(i), 3.0 + 0.12 * i, None
    return rb


