def _load_league_presets():
    """Merge the generic presets with YOUR real leagues from data/league_presets.json
    (generated by import_leagues.py from Sleeper/ESPN)."""
    merged = {k: dict(v) for k, v in PRESETS.items()}
    p = Path(__file__).parent / "data" / "league_presets.json"
    if p.exists():
        try:
            for x in json.load(open(p, encoding="utf-8")).get("presets", []):
                merged[x["name"]] = x
        except Exception:
            pass
    return merged


